    public void createTask(String isChecked, String date, String details) {
        Task t = new Task(isChecked, date, details, this);
        tasks.add(t);

        dao.saveData(t); // save task

        // Show in TaskArea
        TaskArea.add(t);
        TaskArea.revalidate();
        TaskArea.repaint();
    }

    public void delThisTask(Task t) {
        for (int i = 0; i < tasks.size(); i++) {
            if (tasks.get(i) == t)
                tasks.remove(t);
        }
    }

    public Date changetoDateClass(String date) {

        SimpleDateFormat formatter = new SimpleDateFormat("dd/MM/yyyy");

        Date d = null;
        try {
            // แปลง String เป็น Date
            d = formatter.parse(date);
        } catch (ParseException e) {
            e.printStackTrace();
        }
        return d;
    }

    public String formatDate(Date selectedDate) {
        String formattedDate = " ";
        if (selectedDate != null) {
            // แปลงเป็น LocalDate และกำหนดรูปแบบวันที่ที่ต้องการ
            LocalDate localDate = selectedDate.toInstant().atZone(ZoneId.systemDefault()).toLocalDate();
            DateTimeFormatter formatter = DateTimeFormatter.ofPattern("dd/MM/yyyy");
            formattedDate = localDate.format(formatter);
        }
        return formattedDate;
    }

