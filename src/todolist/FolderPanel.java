    public void UpdatePanel(Date date, String details) {
        Task t = new Task(date, details, this);
        tasks.add(t);

        dao.saveData(t);
        t.TaskUpdate();
        t.doNotiTask(t.getIsWarning(), t.getIsChecked());


        // Show in TaskArea
        TaskArea.add(t);
        TaskArea.revalidate();
        TaskArea.repaint();
    }

